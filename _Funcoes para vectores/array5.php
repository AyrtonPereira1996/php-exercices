<?php

// Usando as funcoes array_push e array_pop

    $array = array(23, 34, 20);

    print_r($array);

    array_push($array, 50);

    print_r($array);

    array_pop($array);

    print_r($array);

    array_unshift($array, 5);

    print_r($array);

    array_shift($array);

    print_r($array);

?>